export const createLibrarySong = (song, overrides = {}) => ({
  ...song,
  active: false,
  favorite: false,
  ...overrides,
});

export const getSongById = (id) => songCatalog.find((song) => song.id === id);

const chillHop = () =>
  defaultLibraryIds
    .map((id, index) => {
      const song = getSongById(id);
      return song ? createLibrarySong(song, { active: index === 0 }) : null;
    })
    .filter(Boolean);

export default chillHop;
